namespace Games.IATF.Ring_Target.Scripts
{
	public class RingSettings
	{
		private float height;

		private float width;

		private float positionX;

		private float positionY;

		public float Height
		{
			get
			{
				return height;
			}
			set
			{
				height = value;
			}
		}

		public float Width
		{
			get
			{
				return width;
			}
			set
			{
				width = value;
			}
		}

		public float PositionX
		{
			get
			{
				return positionX;
			}
			set
			{
				positionX = value;
			}
		}

		public float PositionY
		{
			get
			{
				return positionY;
			}
			set
			{
				positionY = value;
			}
		}
	}
}
